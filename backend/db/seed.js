function seed() {
  console.log('Очищаю старые товары...');
  db.exec('DELETE FROM products');

  const insert = db.prepare(`
    INSERT INTO products
      (name, category, price, size, color, description, view_front_text, view_side_text, view_back_text, in_stock)
    VALUES
      (@name, @category, @price, @size, @color, @description, @front, @side, @back, 1)
  `);

  // node:sqlite не имеет готовой обёртки db.transaction(), как в better-sqlite3,
  // поэтому оборачиваем вставку вручную командами BEGIN/COMMIT.
  // Если что-то пойдёт не так — откатываем через ROLLBACK, чтобы
  // в базе не осталась половина товаров.
  db.exec('BEGIN');
  try {
    for (const item of products) insert.run(item);
    db.exec('COMMIT');
  } catch (err) {
    db.exec('ROLLBACK');
    throw err;
  }

  console.log(`Готово! Добавлено товаров: ${products.length}`);
}

if (require.main === module) {
  seed();
}

module.exports = seed;
